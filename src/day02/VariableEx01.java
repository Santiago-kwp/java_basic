package day02;

public class VariableEx01 {
    public static void main(String[] args) {


        // 1. 정수형 변수 value를 선언하시오
        int value;
//         2. 변수 value에 20을 저장(할당) 하세요.
        value = 20;

        // 3. 변수 result에 value 변수에 30을 더한 후 저장핫요.
        int result = value + 30;

        // 4. 변수 result의 값을 콘솔에 출력하세요.
        System.out.println(result);
    }
}
